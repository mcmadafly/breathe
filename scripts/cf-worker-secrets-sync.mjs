#!/usr/bin/env node
/**
 * Generate `cf-worker.secrets.env` from local env files.
 *
 * Precedence (last wins):
 *   1) .env
 *   2) .env.production (optional)
 *
 * This lets you keep local defaults in `.env` and production overrides in
 * `.env.production` without manually copying keys each deploy.
 */
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { parse } from 'dotenv';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const baseEnvPath = path.join(root, '.env');
const prodEnvPath = path.join(root, '.env.production');
const outputPath = path.join(root, 'cf-worker.secrets.env');

// Runtime keys that should be sent to the Worker.
const workerSecretKeys = [
  'AUTH_SECRET',
  'AUTH_TRUST_HOST',
  'TURSO_DATABASE_URL',
  'SKIP_AUTH',
  'FORCE_PRO',
  'TURSO_AUTH_TOKEN',
  'TURSO_USE_EMBEDDED_REPLICA',
  'TURSO_EMBEDDED_REPLICA_PATH',
  'PUBLIC_CLERK_PUBLISHABLE_KEY',
  'CLERK_SECRET_KEY',
  'STRIPE_SECRET_KEY',
  'STRIPE_WEBHOOK_SECRET',
  'STRIPE_PRICE_PRO_MONTHLY',
  'STRIPE_PRICE_PRO_LIFETIME',
];

function parseIfExists(filePath) {
  if (!existsSync(filePath)) return {};
  return parse(readFileSync(filePath, 'utf8'));
}

if (!existsSync(baseEnvPath)) {
  console.error('cf-worker-secrets-sync: missing .env in repo root.');
  process.exit(1);
}

const merged = {
  ...parseIfExists(baseEnvPath),
  ...parseIfExists(prodEnvPath),
};

const missingRequired = ['PUBLIC_CLERK_PUBLISHABLE_KEY', 'CLERK_SECRET_KEY'].filter((k) => !merged[k]);
if (missingRequired.length > 0) {
  console.error(`cf-worker-secrets-sync: missing required key(s): ${missingRequired.join(', ')}`);
  process.exit(1);
}

const lines = [
  '# Auto-generated by scripts/cf-worker-secrets-sync.mjs',
  '# Source precedence: .env -> .env.production (optional override)',
  '',
];

for (const key of workerSecretKeys) {
  const value = merged[key];
  if (value === undefined || value === '') continue;
  lines.push(`${key}=${value}`);
}

writeFileSync(outputPath, `${lines.join('\n')}\n`, 'utf8');

const hasProdOverrides = existsSync(prodEnvPath);
console.log(
  `cf-worker-secrets-sync: wrote ${outputPath} using .env${hasProdOverrides ? ' + .env.production' : ''}.`,
);
